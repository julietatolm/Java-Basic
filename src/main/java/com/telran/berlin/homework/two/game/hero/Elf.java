package com.telran.berlin.homework.two.game.hero;

public class Elf {
    private int id;
    private String name;
    private String surname;
    private int height;
    private int weight;
    private char nick;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getHeight() {
        return height;
    }


    public int getWeight() {
        return weight;
    }

    public char getNick() {
        return nick;
    }

    public void setNick(char nick) {
        this.nick = nick;
    }
}
